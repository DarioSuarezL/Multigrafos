    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String nombreF = JOptionPane.showInputDialog("Ingrese el nombre con el que desea guardar el archivo");
        nombreF += ".json";
        String jsonText = gson.toJson(MG);
        try (PrintWriter pw = new PrintWriter(new File(nombreF))){
            pw.write(jsonText);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(UFPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String nombreF = JOptionPane.showInputDialog("Ingrese el nombre del archivo: \nNOTA: Tiene que ser de formato JSON (no escriba la extensi√≥n)");
        nombreF += ".json";
        nombreArchivo = nombreF;
        try {
            this.jButton6.setEnabled(true);
            MG = gson.fromJson(new FileReader(nombreF), Multigrafo.class);
            actualizarGrafo();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(UFPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

